package ru.itaros.hoe.framework.chemistry;

public class ChemicalElement {

	private String symbol;
	private String name;
	
	public ChemicalElement(String symbol, String name){
		this.symbol=symbol;
		this.name=name;
	}

	public String getSymbol() {
		return symbol;
	}

	public String getName() {
		return name;
	}
	
	
	
}
